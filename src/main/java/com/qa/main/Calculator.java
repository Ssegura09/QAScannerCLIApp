package com.qa.main;

public class Calculator {

	public static int add(int a, int b) {
		return a + b;

	}

	public static int sub(int a, int b) {
		return a - b;
	}
	
	public static int multi(int a, int b) {
		return a * b;

	}
	
	public static int divi(int a, int b) throws ArithmeticException {
		return a / b;

	}

}
